// Loop FOR - Repetição (cada repetição é chamada de iteração)
// loop defindos - qdo soubermos a quantidades(FOR) , indefinidos - qdo não soubermos(WHILE e DO WHILE)

console.log('Início do Programa')
for(let i = 0; i < 20; i++) {
    if(i % 2 == 0) {
        console.log(i + ' é par')
    } else {
        console.log(i + ' é impar')
    }
}
console.log('Fim do Programa')
