/*
Operadores Bitwise - faz a operação bit a bit do elemento
& - retorna 0 -> equivalente ao AND
| - retorna 1 -> eqquivalente ao OR
^ - onde tem equivalência retorna 0 (sendo 0 ou 1), onde não tem retorna 1
>> - desloca a quantidade de bit para a direita
<< - desloca a quantidade de bit para a esquerda
*/

let n1 = 30

res = n1 >> 1

console.log(res)
